def dedup_tags(tags: list[str]) -> list[str]:
    """对标签列表去重：移除低价值标签 + 合并相近标签"""
    # 1. 过滤低价值标签
    filtered = [t for t in tags if t not in LOW_VALUE_TAGS]

    # 2. 收集需要移除的宽泛标签
    to_remove = set()
    for t in filtered:
        if t in TAG_DEDUP:
            to_remove.update(TAG_DEDUP[t])

    # 3. 移除宽泛标签，保持顺序
    result = [t for t in filtered if t not in to_remove]

    # 4. 纯去重（保持顺序）
    seen = set()
    unique = []
    for t in result:
        if t not in seen:
            seen.add(t)
            unique.append(t)

    return unique


def build_prompt_section() -> str:
    """构建嵌入 prompt 的标签列表"""
    return "、".join(TAG_LIBRARY_SORTED)
