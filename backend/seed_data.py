def seed():
    init_db()
    db = SessionLocal()
    try:
        existing = db.query(Game).count()
        if existing > 0:
            print(f"数据库中已有 {existing} 款游戏，跳过种子数据")
            return

        for rank, (steam_app_id, name, desc, img, price, rel_date, tags) in enumerate(SEED_GAMES, 1):
            name_cn = NAME_CN_MAP.get(steam_app_id, "")
            game = Game(
                steam_app_id=steam_app_id,
                name=name,
                name_cn=name_cn,
                description=desc,
                image_url=img or f"/static/images/{steam_app_id}.jpg",
                price=price,
                release_date=rel_date,
            )
            db.add(game)
            db.flush()

            for tag_name in tags:
                tag_name_cn = translate_tag(tag_name)
                tag = db.query(Tag).filter(Tag.name == tag_name_cn).first()
                if not tag:
                    tag = Tag(name=tag_name_cn)
                    db.add(tag)
                    db.flush()
                db.execute(game_tag_assoc.insert().values(game_id=game.id, tag_id=tag.id))

            # 添加到今日热销榜
            db.add(DailyTopSeller(
                game_id=game.id,
                rank=rank,
                date=date.today(),
            ))

        db.commit()
        print(f"种子数据导入完成：{len(SEED_GAMES)} 款游戏，{db.query(Tag).count()} 个标签")
    except Exception as e:
        db.rollback()
        print(f"种子数据导入失败: {e}")
    finally:
        db.close()

if __name__ == "__main__":
    seed()
